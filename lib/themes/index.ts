export const DEFAULT_THEME_ID = 'radiant';

export function getThemeById(id: string): ThemeConfig {
  return THEMES.find((t) => t.id === id) ?? THEMES[0];
}

export function getThemesByCategory(category: ThemeConfig['category']): ThemeConfig[] {
  return THEMES.filter((t) => t.category === category);
}

/**
 * Convert a ThemeConfig into a Record of CSS variable name → value.
 * Inject these as inline style on a container element.
 */
export function themeToCssVars(theme: ThemeConfig): Record<string, string> {
  const radiusMap: Record<string, { sm: string; md: string; lg: string }> = {
    sharp: { sm: '6px', md: '10px', lg: '14px' },
    rounded: { sm: '12px', md: '20px', lg: '28px' },
    pill: { sm: '20px', md: '28px', lg: '999px' },
  };

  const r = radiusMap[theme.borderRadius] ?? radiusMap.rounded;

  return {
    '--bg-base': theme.bgBase,
    '--bg-warm': theme.bgWarm,
    '--text-primary': theme.textPrimary,
    '--text-secondary': theme.textSecondary,
    '--text-muted': theme.textMuted,
    '--accent-primary': theme.accentPrimary,
    '--accent-secondary': theme.accentSecondary,
    '--gradient-primary': theme.gradientPrimary,
    '--gradient-button': theme.gradientButton,
    '--gradient-button-hover': theme.gradientButtonHover,
    '--gradient-soft': theme.gradientSoft,
    '--surface': theme.cardBg,
    '--surface-strong': theme.cardBgStrong,
    '--surface-block': theme.cardBg,
    '--line': theme.cardBorder,
    '--line-accent': theme.cardBorder,
    '--shadow': theme.cardShadow,
    '--shadow-glow': theme.shadowGlow,
    '--shadow-glow-warm': theme.shadowGlowWarm,
    '--radius-sm': r.sm,
    '--radius': r.md,
    '--radius-lg': r.lg,
  };
}
