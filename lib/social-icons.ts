export function detectPlatform(url: string, label: string): { viewBox: string; path: string } {
  const lowerLabel = label.toLowerCase();

  // Try URL pattern first (skip the generic fallback)
  for (let i = 0; i < SOCIAL_PLATFORMS.length - 1; i++) {
    if (SOCIAL_PLATFORMS[i].pattern.test(url)) return SOCIAL_PLATFORMS[i];
  }

  // Then try label match
  for (let i = 0; i < SOCIAL_PLATFORMS.length - 1; i++) {
    if (SOCIAL_PLATFORMS[i].labels.some((l) => lowerLabel.includes(l))) return SOCIAL_PLATFORMS[i];
  }

  // Fallback: generic link icon
  return SOCIAL_PLATFORMS[SOCIAL_PLATFORMS.length - 1];
}
